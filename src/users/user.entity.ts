import { PrimaryGeneratedColumn, BaseEntity, Column, Entity } from "typeorm";

@Entity()
export class User extends BaseEntity {
  @PrimaryGeneratedColumn()
  id: number;

  @Column()
  name: string;

  @Column()
  email: string;

  @Column()
  password: string;

  
  @Column({default:''})
  eotp:string = ''

}